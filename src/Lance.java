public class Lance {
    
    private int lance1;
    private int lance2;

    public Lance(int lance1, int lance2) {
        this.lance1 = lance1;
        this.lance2 = lance2;
    }

    public int getLance1() {
        return lance1;
    }

    public void setLance1(int lance1) {
        this.lance1 = lance1;
    }

    public int getLance2() {
        return lance2;
    }

    public void setLance2(int lance2) {
        this.lance2 = lance2;
    }

}
